BEGIN TRANSACTION;
CREATE TABLE IF NOT EXISTS "tbl_agents" (
	"id"	VARCHAR(3) NOT NULL UNIQUE,
	"name"	TEXT NOT NULL,
	"commission"	INTEGER NOT NULL,
	"jp_factor"	INTEGER NOT NULL,
	"sp_factor"	INTEGER NOT NULL,
	"created_at"	TEXT NOT NULL DEFAULT CURRENT_TIMESTAMP,
	"notes"	TEXT,
	PRIMARY KEY("id")
);
CREATE TABLE IF NOT EXISTS "tbl_blacklist_tickets" (
	"id"	INTEGER NOT NULL,
	"draw_id"	INTEGER NOT NULL,
	"ticket"	VARCHAR(3) NOT NULL CHECK("ticket" GLOB '[0-9][0-9][0-9]'),
	"type"	TEXT NOT NULL DEFAULT 'HALF' CHECK("type" IN ('HALF', 'FULL')),
	"created_at"	TEXT DEFAULT CURRENT_TIMESTAMP,
	PRIMARY KEY("id" AUTOINCREMENT),
	FOREIGN KEY("draw_id") REFERENCES "tbl_draws"("id") ON DELETE RESTRICT
);
CREATE TABLE IF NOT EXISTS "tbl_draws" (
	"id"	INTEGER NOT NULL,
	"draw_date"	TEXT NOT NULL,
	"status"	TEXT NOT NULL DEFAULT 'Open' CHECK("status" IN ('Open', 'Closed', 'Settled')),
	"global_limit"	INTEGER NOT NULL,
	"created_at"	TEXT DEFAULT CURRENT_TIMESTAMP,
	PRIMARY KEY("id" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "tbl_master_dealers" (
	"id"	VARCHAR(3) NOT NULL,
	"name"	TEXT NOT NULL,
	"commission"	INTEGER NOT NULL,
	"jp_factor"	INTEGER NOT NULL,
	"sp_factor"	INTEGER NOT NULL,
	"created_at"	TEXT DEFAULT CURRENT_TIMESTAMP,
	"notes"	TEXT,
	PRIMARY KEY("id")
);
CREATE TABLE IF NOT EXISTS "tbl_offloaded_records" (
	"id"	INTEGER NOT NULL,
	"draw_id"	INTEGER NOT NULL,
	"master_dealer_id"	VARCHAR(3) NOT NULL,
	"brochure_no"	INTEGER NOT NULL,
	"ticket"	VARCHAR(3) NOT NULL CHECK("ticket" GLOB '[0-9][0-9][0-9]'),
	"amount"	INTEGER NOT NULL CHECK("amount" > 0),
	"created_at"	TEXT DEFAULT CURRENT_TIMESTAMP,
	"notes"	TEXT,
	PRIMARY KEY("id" AUTOINCREMENT),
	FOREIGN KEY("draw_id") REFERENCES "tbl_draws"("id") ON DELETE RESTRICT,
	FOREIGN KEY("master_dealer_id") REFERENCES "tbl_master_dealers"("id") ON DELETE REsTRICT
);
CREATE TABLE IF NOT EXISTS "tbl_sales" (
	"id"	INTEGER NOT NULL,
	"draw_id"	INTEGER NOT NULL,
	"agent_id"	VARCHAR(3) NOT NULL,
	"brochure_no"	INTEGER NOT NULL,
	"ticket"	VARCHAR(3) NOT NULL CHECK("ticket" GLOB '[0-9][0-9][0-9]'),
	"amount"	INTEGER NOT NULL CHECK("amount" > 0),
	"created_at"	TEXT DEFAULT CURRENT_TIMESTAMP,
	"notes"	TEXT,
	PRIMARY KEY("id" AUTOINCREMENT),
	FOREIGN KEY("agent_id") REFERENCES "tbl_agents"("id") ON DELETE RESTRICT,
	FOREIGN KEY("draw_id") REFERENCES "tbl_draws"("id") ON DELETE RESTRICT
);
CREATE TABLE IF NOT EXISTS "tbl_winning_tickets" (
	"id"	INTEGER NOT NULL,
	"draw_id"	INTEGER NOT NULL,
	"ticket"	VARCHAR(3) NOT NULL CHECK("ticket" GLOB '[0-9][0-9][0-9]'),
	"prize_type"	TEXT NOT NULL CHECK("prize_type" IN ('Jackpot', 'Minor')),
	"created_at"	TEXT DEFAULT CURRENT_TIMESTAMP,
	"notes"	TEXT,
	PRIMARY KEY("id" AUTOINCREMENT),
	FOREIGN KEY("draw_id") REFERENCES "tbl_draws"("id") ON DELETE RESTRICT
);
